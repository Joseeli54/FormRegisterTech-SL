     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $company = new Company();

        $company->user_id = $request->input('user_id');
        $company->name = $request->input('name');
        $company->phone = $request->input('phone');
        $company->email = $request->input('email');
        $company->findout = $request->input('findout');
        $company->revenueve = $request->input('revenueve');
        $company->team = $request->input('team');
        $company->obstacle = $request->input('obstacle');
        $company->business = $request->input('business');
        $company->save();

        return 'Register Successfully Company';

    }

